//! The CRC id's protocol runs on: CCITT-16 ([`block`]), and the per-`clc_move` sequence byte
//! ([`block_sequence_crc_byte`]) that proves to the server we're a real client.
//!
//! `block` is a plain CRC-16/CCITT-FALSE (poly `0x1021`, init `0xFFFF`, no reflection, no final
//! XOR). The `chktbl` below and the sequence-byte construction are id's, ported from qualia's
//! `src/crypto/crc_extra.cppm` — which carries id's `GPL-2.0` notice, compatible with this
//! crate's AGPL-3.0-or-later. The 512 table bytes were verified byte-for-byte against ezQuake's
//! independent copy (`src/common.c`).
//!
//! One quirk is load-bearing: id declared `chktbl` as **1024 bytes but initialized only 512**, so
//! the upper half is zeros — and since the salt offset is `sequence % 1020`, that zero half is
//! genuinely part of the checksum for most sequence numbers. C gave it to them for free; here
//! [`CHKTBL`] spells it out by zero-extending [`CHKTBL_HEAD`]. A "tidied" 512-byte table would
//! fail against a real server roughly half the time.

/// CRC-16/CCITT-FALSE, as `CRC_Block`. Used for the `clc_move` sequence byte and for the
/// `pmodel`/`emodel` userinfo checksums of `progs/player.mdl` and `progs/eyes.mdl`.
pub fn block(data: &[u8]) -> u16 {
    let mut crc: u16 = 0xffff;
    for &b in data {
        crc = (crc << 8) ^ TABLE[((crc >> 8) as u8 ^ b) as usize];
    }
    crc
}

/// The CRC-16/CCITT table, built at compile time from the polynomial.
const TABLE: [u16; 256] = {
    let mut table = [0u16; 256];
    let mut i = 0;
    while i < 256 {
        let mut crc = (i as u16) << 8;
        let mut j = 0;
        while j < 8 {
            crc = if crc & 0x8000 != 0 {
                (crc << 1) ^ 0x1021
            } else {
                crc << 1
            };
            j += 1;
        }
        table[i] = crc;
        i += 1;
    }
    table
};

