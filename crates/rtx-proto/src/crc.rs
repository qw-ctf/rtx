/// id's `chktbl`: [`CHKTBL_HEAD`] zero-extended to the 1024 bytes C gave it (see the module note).
static CHKTBL: [u8; 1024] = {
    let mut t = [0u8; 1024];
    let mut i = 0;
    while i < CHKTBL_HEAD.len() {
        t[i] = CHKTBL_HEAD[i];
        i += 1;
    }
    t
};

/// `COM_BlockSequenceCRCByte` — the checksum byte at the head of every `clc_move`.
///
/// Mixes up to 60 bytes of the move payload with four salt bytes drawn from [`CHKTBL`] at an
/// offset keyed by the packet's outgoing sequence, then takes the low byte of the CRC. The server
/// recomputes it and ignores the packet on a mismatch, so this is effectively the handshake that
/// says "a real QuakeWorld client wrote this move".
pub fn block_sequence_crc_byte(payload: &[u8], sequence: u32) -> u8 {
    let mut chkb = [0u8; 60 + 4];
    let len = payload.len().min(60);
    chkb[..len].copy_from_slice(&payload[..len]);

    // `% 1020` (not 1024) keeps all four salt reads in bounds at every sequence number.
    let p = &CHKTBL[(sequence as usize % (CHKTBL.len() - 4))..];
    chkb[len] = (sequence & 0xff) as u8 ^ p[0];
    chkb[len + 1] = p[1];
    chkb[len + 2] = ((sequence >> 8) & 0xff) as u8 ^ p[2];
    chkb[len + 3] = p[3];

    (block(&chkb[..len + 4]) & 0xff) as u8
}

#[cfg(test)]
mod tests {
    use super::*;

    /// Pin CRC-16/CCITT-FALSE against its published check vector. If this drifts, every checksum
    /// in the crate is wrong in the same direction and nothing else would notice.
    #[test]
    fn crc16_matches_ccitt_false_check_vector() {
        assert_eq!(block(b"123456789"), 0x29b1);
        assert_eq!(block(&[]), 0xffff);
    }

    /// The zero tail is load-bearing: for most sequence numbers the salt is drawn from it. Guard
    /// the table's shape so a well-meaning cleanup can't silently break half the packets.
    #[test]
    fn chktbl_is_1024_bytes_with_a_zero_tail() {
        assert_eq!(CHKTBL.len(), 1024);
        assert_eq!(CHKTBL[0], 0x78);
        assert_eq!(CHKTBL[511], 0xd3);
        assert!(CHKTBL[512..].iter().all(|&b| b == 0));
    }

    /// The sequence byte must depend on the sequence — that's what stops a replayed move packet —
    /// and must be stable for a given (payload, sequence).
    #[test]
    fn sequence_byte_varies_with_sequence() {
        let payload = b"\x00\x01\x02\x03";
        let a = block_sequence_crc_byte(payload, 1);
        let b = block_sequence_crc_byte(payload, 2);
        assert_ne!(a, b);
        assert_eq!(a, block_sequence_crc_byte(payload, 1));
    }

    /// Sequence numbers run for hours; the modulus must keep the salt reads in bounds at every one
    /// of them, including past the wrap.
    #[test]
    fn sequence_byte_is_in_bounds_at_every_sequence() {
        for seq in (0u32..2048).chain([u32::MAX - 1, u32::MAX]) {
            block_sequence_crc_byte(b"payload", seq);
        }
    }

    /// A payload longer than 60 bytes is truncated, not rejected — a `clc_move` with three large
    /// usercmds exceeds 60, so this path runs in every real packet.
    #[test]
    fn sequence_byte_truncates_long_payloads() {
        let long = [0xabu8; 200];
        assert_eq!(
            block_sequence_crc_byte(&long, 7),
            block_sequence_crc_byte(&long[..60], 7)
        );
    }
}
